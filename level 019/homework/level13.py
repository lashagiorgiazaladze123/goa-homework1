# Examples with AND operator
print(True and True)         
print(False and True)        
print(True and (5 > 3))       
print((4 == 4) and (2 != 5))  
print((10 > 2) and (3 < 4))   
print((1 < 2) and False)     
print((0 == 0) and (1 != 2))  
print((100 > 50) and (50 > 100)) 
print((3 < 5) and (2 < 1)) 



# Examples with OR operator
print(False or True)         
print((3 > 2) or (5 == 5))   
print((7 == 8) or True)      
print((10 < 5) or (6 != 6))  
print(False or False)        
print((1 == 1) or (0 > 1))   
print((4 <= 3) or (5 >= 5))  
print((10 == 10) or (1 == 2)) 
print((3 > 5) or (4 < 6))    
print((0 != 1) or (2 != 2))  